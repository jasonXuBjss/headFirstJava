package org.example;

public class Loopy{
    public static void main(String[] args) {
        int x = 1;
        while (x < 3) {
            System.out.printf("Doo");
            System.out.printf("Bee");
            x = x  + 1;
        }

        if (x ==3 ) {
            System.out.printf("Do");
        }
    }
}
